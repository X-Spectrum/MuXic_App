import 'dart:core';

class Music{

  String title = "";
  String artist = "";
  String path = "";

  Music(String t, String a, String p){
    title = t;
    artist = a;
    path = p;
  }

  String getTitle(){
    return title;
  }

  String getArtist(){
    return title;
  }

  String getPath(){
    return title;
  }
}